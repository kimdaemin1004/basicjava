
public class Ex01 {
	public static void main(String[] args) {
		
		for(int i = 0; i <= 10; i++ ) { //(시작,초기화식 ; 끝,조건식 ; 스텝,증감식)
			System.out.print(i + ""); // 11번 반복			
		}
		System.out.println("\n=======================");
		
		for(int i = 0; i <= 10; i+=2 ) { // 
			System.out.print(i + ""); // 6번 반복			
		}
		System.out.println("\n===================");
		
	}

}
